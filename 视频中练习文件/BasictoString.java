// 基本类型 -> String
public class BasictoString{
	public static void main(String[] args){
		int a = 100;
		short b = 100;
		float c = 1.1F;
		double d = 4.5;
		boolean e = true;
		String s1 = a + "";
		String s2 = b + "";
		String s3 = c + "";
		String s4 = d + "";
		String s5 = e + "";
		System.out.println(s1 + "\n" + s2 + "\n" + s3 + "\n" + s4 + "\n" +s5);
		System.out.println("-------------");
	}
}